export function addTwoNumbers(num1: number, num2: number) {
    return num1 + num2;
}

export function substracTowNumbers(num1: number, num2: number) {
    return num1 - num2;
}

export function timesTowNumbers(num1: number, num2: number) {
    return num1 * num2;
}

export function divideTowNumbers(num1: number, num2: number) {
    return num1 / num2;
}


